#Python PIP - Gerenciador de pacotes Python

#PIP - Programador Instalador Preferencial. O pip é o modulo 
#em python ele é o mais famoso para instalar pacotes.

#check pip version
#pip --version

#Instalando pacotes usando pip
#Vamos instalar numpy, poderoso pacote para computacao cientifica.
#pip install numpy

#Desinstalando pacotes
#pip unistall numpy

#Lista de pacotes
#pip list

#Mostrar pacote
#pip show packagename
#Se quiser mais informacao:
#pip show --verbose packagename

#Mostrar pacotes utilizados, por exemplo para criar
# um arquivo requerimentos.txt
#pip freeze
#O pip freeze nos deu os pacotes usados, instalados e 
#sua versão. Nós o usamos com o arquivo requirements.txt 
#para implantação.